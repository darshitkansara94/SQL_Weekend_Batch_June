XMl Datatype :
	Extension Markup Language
	Has a parent and child node relation.
	We can create a tag with the custom name.

	-- Syntax :
		<Employee>
			<Id>1</Id>
			<Name>Abc</Name>
			<Age>20</Age>
		</Employee>

		-- Multiple values
			<Employees>
				<Employee>
					<Id>1</Id>
					<Name>Abc</Name>
					<Age>20</Age>
				</Employee>
				<Employee>
					<Id>2</Id>
					<Name>xyz</Name>
					<Age>15</Age>
				</Employee>
			</Employees>