Distinct :
	Combine a duplicate values and return as a single value.
	Work with Select statement.
	Recommand not to use distinct.
	-- Syntax :
		Select Distinct column1, column2 from table_name
	
	-- Example

	-- use EmployeeDB
	Select * from tbl_EmployeeData 

	Select distinct employee_name,Employee_Age from tbl_EmployeeData